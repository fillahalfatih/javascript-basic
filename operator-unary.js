
/*
  Operator Unary
  + --> positive
  - --> Negative
  ++ --> Increment (Menaikkan 1 angka)
  -- --> Decrement (Menurunkn 1 angka)
  ...
*/

let result = 1;
document.writeln(`<p>${result}</p>`);

result--; // result -= 1; 0
document.writeln(`<p>${result}</p>`)

result++; // result += 1; 1
document.writeln(`<p>${result}</p>`)

result = -result; // -1
document.writeln(`<p>${result}</p>`)
